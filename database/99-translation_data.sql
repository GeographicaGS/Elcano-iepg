institución–. El cuestionario fue enviado a 45 instituciones norteamericanas, 40 europeas, 27 asiáticas, 17 
latinoamericanas, 12 africanas, 8 de Oriente Medio y Norte de África, y 3 de Oceanía.</p>

<p>A todas ellas se remitió una encuesta con el propósito de asignar una ponderación concreta a cada 
